                <h2>What are the server rules?</h2>
                <p>The /rules command lays it out quite nicely:<br></p>
                <ol>
                    <li>Be respectful to staff and others</li>
                    <li>No cheating/hacking/use of mods</li>
                    <li>No swearing or spam in chat</li>
                    <li>Have fun!</li>
                </ol>
                <p>However, some of these rules are vague so here is an explanation of each...</p>
                <h3>Be respectful of staff and others</h3>
                <p>Bullying of any form will not be tolerated on our server. If someone is being derogative towards you or another player, report it by going to the player's profile like: <a href="https://absolutecraft.co.uk/players">https://absolutecraft.co.uk/player/USERNAME</a> or contact a Moderator or Operator on the server and the player(s) will be dealt with swiftly.</p>
                <p>Respect our staff. If you believe a Moderator is breaking the rules or misusing their permissions, collect evidence and contact an Operator.</p>
                <p>Our staff do not get paid to moderate the server, they have offered up their own free time to help out. Do not abuse them, in the same way we expect you not to be abusive to one another.</p>
                <h3>No cheating/hacking/use of mods</h3>
                <p>Now we understand how this can be vague! Because some mods are fun here is an example list of what type of mods are not allowed and what are not allowed on the server.</p>
                <table class="table table-striped">
                    <thead>
                        <tr>
                            <td><h4>Allowed</h4></td>
                            <td><h4>Not Allowed</h4></td>
                        </tr>
                    </thead>
                    <tbody>
                        <tr>
                            <td>Forge Modloader</td>
                            <td>Any hack mod<br>Such as water-walking, X-ray, flight, sprinthacks, etc.</td>
                        </tr>
                        <tr>
                            <td>Shaders<br>Any mod that improves how the game looks, but do not change any mechanics</td>
                            <td>Smart Moving Mod<br>Provides unnatural movement</td>
                        </tr>
                        <tr>
                            <td>Optifine</td>
                            <td>X-Ray Mods<br>Any mod which allows you to see through blocks</td>
                        </tr>
                        <tr>
                            <td>Rei's Minimap</td>
                            <td>A minimap with an entity radar<br>Any mod which indicates where mobs or players are</td>
                        </tr>
                        <tr>
                            <td>Advanced HUD</td>
                            <td>Damage Indicators<br>Shows information you technically shouldn't know</td>
                        </tr>
                        <tr>
                            <td>TooManyItems</td>
                            <td>Chest Finder</td>
                        </tr>
                        <tr>
                            <td>Fullbright Mod</td>
                            <td>Zombe's Mod<br>Some of these features are really useful but it includes things like fly and x-ray so it's banned!</td>
                        </tr>
                    </tbody>
                </table>
                <p>Also, no exploiting any sort of glitches which are not meant to be in the map or plugin. This may include being able to get outside of the map or overlap regions in Survival.</p>
                <h3>No swearing or spam in chat</h3>
                <p>We do not appreciate spam of any type. The minimal of this is sending 3 of the same message consecutively within the space of a few seconds. Spam also includes garbage messages, posting many punctuation marks in a single message, or single lines all of one number/letter/symbol.</p>
                <p>We do not allow for the use of ALL CAPS in game. It spams and clutters the chat and furthermore it is annoying. You wouldn't go around shouting all the time in real life so don't do it in game.</p>
                <p>No constant swearing as we have players of all different ages and Minecraft is a family game. So we will not tolerate any foul language, just remember to keep it PG-13.</p>
                <p>Do not advertise any links to websites or other Minecraft servers outside the topic of conversation.</p>
                <h3>Have fun!</h3>
                <p>And last of all, have fun!</p>
                <p>There's no point to any of these rules if you can't have fun. AbsoluteCraft hosts players from across the globe, from many different countries, religions and races so just be respectful to other players and use common sense, the summary of rules are so simple you may not even need to read the above!</p>
